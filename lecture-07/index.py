# python type casting
username = str(' abdullah jutt ')
# slicing strings

print(username[0:5])

# slicing from the start

print(username[:5])

# slicing from the end

print(username[5:])

# negative slicing

print(username[-1:-3])

# modify strings

# uper string

print(f"Here is your Uppercase String: {username.upper()}")

# lower case string

print(f"Here is your Lower Case String: {username.lower()}")


print(username)

# total characters

print(len(username))

# remove whitespace

print(username)
print(username.strip())

#   REPLACE specific character from string

print(username.replace('abdullah', 'yousaf'))

